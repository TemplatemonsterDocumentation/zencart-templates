<h2 class="item6">Theme customization</h2>

<h4>How to change store logo</h4>

<p>To use your own logo, simply overwrite 'logo.jpg'
    <em>('logo.png')</em>
    that is located in <b>includes/templates/themeXXX/images/</b>.
</p>

<p>Upload a new image with the same title to the same folder on server, accepting the replacement.</p>

<p>You can create an image that is larger than the default one. If it doesn't match the header dimensions, you'll need
    to perform style modifications in the <b>/includes/templates/themeXXX/css/stylesheet_tm.css</b> file. <a
        href="http://www.templatemonster.com/help/developer-tools.html">You can locate the needed css rule with the help
        of developer tools</a>.</p>

<h4>How to change store colors</h4>

<p>To change the site colors, you need to edit the CSS files in the <b>/includes/templates/themeXXX/css/</b>. You can
    learn more about working with the CSS files by <a href="/help/general-tutorials/work-with-css/" target="_blank">checking
        the detailed video tutorials</a>. </p>

<h4>How to increase the number of characters in the banner's title</h4>

<p>By default the number of characters for the banner's title is 64 and if the title exceeds this value the rest of the
    content is simply not displayed. To change this value go to: phpMyAdmin - Structure > Banners - Structure >
    banners_title > Change > Length/Values > 512 => Save. For correct display this will be enough.</p>

