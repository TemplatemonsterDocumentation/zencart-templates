<h3>Extracting Files</h3>
<p>Once the Zen Cart engine package is downloaded, you will need to extract its files and folders from the archive.</p>
<p>This can be done with the required software like <b>WinZip 9+ (Windows)</b> and <b>Stuffit Expander 10+ (Mac)</b>.
</p>
<p>Please check the following tutorials on how to extract files from the archives with <a
        href="/help/extract-archived-files-windows-os.html" target="_blank">WinZip(PC)</a> and <a target="_blank"
                                                                                                  href="http://www.templatemonster.com/help/extract-archived-files-mac-os.html">StuffitExpander(MAC)</a>.
</p>
<p class="alert alert-warning">Please note: if your hosting control panel has the option to <b>'Extract'</b> files, you can
    use it to extract files directly on your server after the .zip file is uploaded. </p>