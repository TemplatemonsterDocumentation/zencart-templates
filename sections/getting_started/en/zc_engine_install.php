<h3>Zen Cart engine installation</h3>
<p>When you are done with the files upload, you can start Zen Cart engine installation. Open your web browser and type
    in the domain name/path to your Zen Cart directory in the address bar and reload the current page. You should see
    the welcome screen. Please follow the instructions below to install Zen Cart.</p>
<h4>Installation Assistant</h4>
<p>Click the “Click here to begin the installation” link. </p>
<figure class="img-polaroid">
    <img src="img/01-install.png" alt=""/>
</figure>

<h4>Installation Assistant (License agreements)</h4>
<p>Please check the license agreement.</p>
<p>When you are done, click 'Continue' button.</p>
<figure class="img-polaroid">
    <img src="img/01-install-1.png" alt=""/>
</figure>

<h4>Installation Assistant (License Confirmation)</h4>
<p>When you are done, select 'I have read and agree to abide by the Terms and Conditions as stated above' and click
    'Continue' button.</p>
<figure class="img-polaroid">
    <img src="img/01-install-2.png" alt=""/>
</figure>

<h4>System Inspection</h4>
<p>Make sure your hosting server matches the ZenCart engine requirements. Otherwise you’ll see the error message.
    Contact your hosting provider if the System Inspection screen shows any errors. If everything is OK, click Install
    button at the bottom of the screen. </p>
<p>If everything is correct, click the 'Install' button.</p>
<figure class="img-polaroid">
    <img src="img/02-install.png" alt=""/>
</figure>

<h4>Database Setup</h4>
<p>At the Database setup step you need to input your database details. Note that you should have a new database created.
    Feel free to check the detailed tutorial on <a href="/help/how-to-create-a-database.html" target="_blank">how to create a
        database</a>.</p>
<p>You need to input the database host name, user name, password and database name. All these details can be obtained
    from your hosting control panel or by contacting your hosting provider. </p>
<p>When you are done, click <b>'Save Database Settings'</b> button.</p>
<figure class="img-polaroid">
    <img src="img/03-install.png" alt=""/>
</figure>

<h4>System Setup</h4>
<p>At the System Setup step you can define the root to your ZenCart installation and install SSL certificate. If you are
    not sure, please do not perform any changes at this step. Click the Save System Settings at the bottom of the screen
    to proceed. </p>
<p>When you are done, click <b>'Save System Settings'</b> button.</p>
<figure class="img-polaroid">
    <img src="img/03-install-1.png" alt=""/>
</figure>

<h4>Store Setup</h4>
<p>Store setup step lets you input the store info like store name, country and region. The Store demo at the bottom
    allows you to install the default ZenCart sample data – sample products that allow to test the store functionality.
    If you are using purchased ZenCart template, there is no need to install default demo store. The sample products are
    provided within the template package.</p>
<p>When you are done, click <b>'Save Store Settings'</b> button.</p>
<figure class="img-polaroid">
    <img src="img/04-install.png" alt=""/>
</figure>

<h4>Administrator Account Setup</h4>
<p>At the Administrator Account Setup step you need to create the store administrator account. Input Administrator user
    name, email and password. Keep these details safe to avoid unauthorized access. </p>
<p>When you are done, click <b>'Save Admin Settings'</b> button.</p>
<figure class="img-polaroid">
    <img src="img/05-install.png" alt=""/>
</figure>

<h4>Setup - Finished</h4>
<p>Congratulations. You’ve successfully installed ZenCart store. Use the buttons at the bottom of the screen to access
    the store frontend or the ZenCart administration panel. </p>

<figure class="img-polaroid">
    <img src="img/06-install.png" alt=""/>
</figure>

<p class="alert alert-danger">For security reasons it is highly recommended setting permissions for the configuration
    files to "Read only" and renaming the admin folder.</p>

<p>Now you are ready to proceed to configuring your Zen Cart based Web site. Click '<b>Click here to go to the
        Store</b>' button to view your Web site or '<b>Click here to open the Admin area</b>' to view Zen Cart
    backend.</p>
<p class="alert alert-info">Please check the detailed video tutorial on <a href="/help/zencart-engine-installation.html" target="_blank">how
        to install Zen Cart engine</a>. </p>