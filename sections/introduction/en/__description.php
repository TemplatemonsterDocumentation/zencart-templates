<h2 class="item1">Introduction</h2>

<h5>Thank you for purchasing Zen Cart template. This documentation consists of several parts and shows you the entire
    process how to set up and administer Zen Cart Web site from scratch. We did our best to make this manual as clear
    and easy to follow as possible. </h5>

<h3>What is Zen Cart?</h3>
<p>Zen Cart is an Open Source e-commerce application. It is written in PHP and requires a MySQL database. Zen Cart
    provides easy customization of your online store. Zen Cart has a built-in system for receiving online payments,
    setting tax and discount rates, etc. <a href="http://www.zen-cart.com" target="_blank">Learn More</a></p>

<h3>What is Zen Cart template</h3>
<p>Zen Cart template is a skin for your Zen Cart platform. In other words you can easily change your Zen Cart Web site
    appearance by installing a new template in a few easy steps. With all its simplicity Zen Cart template is provided
    with all necessary source files and you are free to edit or extend it the way you need.</p>

