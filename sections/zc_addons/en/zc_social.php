<h3>Social Media Icons Module</h3>
<p>This module enables the display of social network icons on the product page as well as the option of sharing your
    product info in the social network sites (Goolge Plus, Facebook, Twitter, Pinterest).</p>
<figure class="img-polaroid">
    <img src="img/zc_social_icons.png" alt="">
</figure>
<p><b>Module Setup:</b> To set up Social Media Icons Module, import of <b>ZENCART_SOCIAL.sql</b> file is required. The
    SQL patch can be imported using the Zen Cart <b>Admin panel > Tools > Insall SQL Patches</b> option.</p>
<p><b>Configuration:</b> To configure the module, please go to <b>Configuration > Social Media Icons</b> in Zen Cart
    admin. All icons are active by default.</p>
<p>Deactivate all icons - Social Media Buttons - set value to 0 Switch on/off certain icons: choose icon title - set
    value to 1 or 0 (1 = on, 0 = off).</p>
