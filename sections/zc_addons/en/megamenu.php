<h3>Megamenu</h3>
<p>Megamenu is a custom navigation component that uses the standard Navbar markup and contains additional links of your
    website such as Categories, Services, Quick links, Manufacturers, Info, Shipping & Returns. Also, you can use
    description text & carousel elements for Specials, custom banners for Categories, Images for Manufacturers, label
    text, resizable columns, etc. Menu is compatible with mobile devices and popular web browsers.</p>
