<h3>Ajax Search</h3>
<p>This module was added to the template to extend functionality of the default search. Ajax Search allows you to find
    the right content and immediately display it without reloading the page under the search field. The results are
    presented with images, so they look more attractive.</p>
<p>In order to add this Ajax-search to your website, it is enough to install ZENCART_AJAX_SEARCH.sql via <b>Admin panel
        > Tools > Install SQL Patches</b>. Additional settings are not required.</p>
