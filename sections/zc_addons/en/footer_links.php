    <img src="img/zc_footer_links_1.png" alt="">
</figure>
<p class="alert alert-info">For example:<br>
    in the <b>first</b> column the links with sort order 11, 12, 13, 14, 15 will be displayed<br>
    in the <b>second</b> column the links with the sort order set to 21, 22, 23, 24, 25 will be displayed<br>
    in the <b>third</b> one the links with the sort order set to 31, 32, 33, 34, 35 will be displayed, etc.</p>
