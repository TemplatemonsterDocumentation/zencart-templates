<h3>Footer Links in Columns Module</h3>
<p>The module enables the display of footer links in lines as well as in columns. The number of columns and the position
    of every item in the column are defined by the order that is set in Admin > Tools > EZ-Pages.</p>
<p>The first digit in the sort order sets the column number for the link location, the number of columns depends on the
    quantity of first digits available. The second digit in the sort order defines the link location in the column.</p>
