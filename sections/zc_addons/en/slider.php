<h3>Slider</h3>
<p>This feature allows to add frontend banners with slideshow effect.</p>
<p>You can navigate to <strong>Configuration > ZC Slideshow</strong> to configure the slider:</p>
<figure class="img-polaroid">
    <img src="img/zc_slider1.png" alt="Slider">
</figure>
<p>Slider provides several functions that can be changed manually:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Slideshow Theme</dt>
            <dd>Allows you to select one of four themes of your slider;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Effect</dt>
            <dd>Sets the transition options between slides;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Animation Speed</dt>
            <dd>Slideshow transition speed in milliseconds;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Pause Time</dt>
            <dd>How long each slide will show in milliseconds;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Navigation Arrows</dt>
            <dd>Show Prev/Next navigation arrows;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Navigation Hide</dt>
            <dd>Show Prev/Next navigation arrows only on hover;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Numbered Navigation</dt>
            <dd>Show numbered navigation;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Pause on Hover</dt>
            <dd>Stop animation while hovering;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Slideshow Captions</dt>
            <dd>Caption opacity (set it to 0 to make it invisible).</dd>
        </dl>
    </li>
</ul>
<p>You can add all the required slider effects and select the group of banners that will show up in your slider.</p>
<p class="alert alert-warning">NOTE: After installing ZENCART_SLIDER.sql or ZENCART_MEGAMENU.sql you need to name the
    appropriate group in the Banner Manager. Navigate to Tools > Banner Manager > new banner > Banner group > and name
    the field "slider", otherwise your slider will not display. The same applies to the Megamenu, but with
    "category_banner" name.</p>
