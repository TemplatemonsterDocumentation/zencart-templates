<h3>Extra Product Images</h3>

<p>The new Zen-cart feature allows you to display the additional product images on the product page and open them in the
    lightbox. To make the extra images display on the product page, please put them into the folder with the product
    images.</p>
<ol class="marked-list">
    <li>Open Zen Cart admin panel</li>
    <li>Go to <b>Catalog</b> > On the product editing screen scroll down and find the image file name and open the
        product you want to modify.
    </li>
    <li>On the product editing screen scroll down and find the image file name.</li>
</ol>
<figure class="img-polaroid">
    <img src="img/zc_extra_img.png" alt="">
</figure>
<p>Create new product images with the names that contain the default product image file name.</p>
<p class="alert alert-info">For example:<br>
    Default product image name: <b>01.png</b><br>
    Additional product image names: <b>01_1.png, 01-2.png</b>, etc.<br>
    or<br>
    Default product image name: <b>my_product.jpg</b><br>
    Additional product image names: <b>my_product_1.png, my_product-2.png</b>, etc.
</p>
<p>Upload the additional images to the directory where the default image is located. Usually the product images are
    stored in the '<b>images</b>' directory of your Zen Cart installation.</p>
<p>When the upload is complete, open the product page in the browser and refresh it. You should see additional images at
    the bottom of the page. </p>
<p>The number of additional images is unlimited.</p>
