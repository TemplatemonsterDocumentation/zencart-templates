<h2 class="item5">How to manage modules</h2>

<p>To manage Left/Right boxes, open Zen Cart admin panel > <b>Tools > Layout Boxes Controller</b>. </p>
<p>You will see the list of available modules. Select one, then in the right panel click <b>'Edit'</b> button.</p>
<figure class="img-polaroid">
    <img src="img/zc_modules.png" alt="">
</figure>
<p>When you use both left and right columns, you can change the following properties:</p>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Left/Right Column Status:</dt>
            <dd>ON=visible, OFF=hidden</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Location:</dt>
            <dd>LEFT or RIGHT position of the sidebox</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Left/Right Column Sort Order:</dt>
            <dd>sort order of sideboxes in the columns.</dd>
        </dl>
    </li>
</ul>
<p>Note that Single Column settings do not currently affect the page display, and can generally be ignored.</p>
<figure class="img-polaroid">
    <img src="img/zc_modules_1.png" alt="">
</figure>
<p class="alert alert-info">If you do not need any side boxes on this site, please navigate to 'Configuration', then
    'Layout Settings', and set the 'Column Left Status - Global', 'Column Right Status - Global' to '0'. This will turn
    off both left and right side boxes globally, even if any of them is set to 'on' under 'Layout Boxes Controller'.</p>