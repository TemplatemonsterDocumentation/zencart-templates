<h2 class="item2">Prepare</h2>

<h6>Before you proceed to setting up your Zen Cart Web site, please make sure you are fully prepared. Please complete the
    following preparation steps:</h6>


<h4>Editing software</h4>
<p>To feel comfortable working with Zen Cart template, we recommend downloading all applications required. You can see
    the list of required software on the template preview page. <br>The requirements may vary for different templates so
    we'll tell you here what is needed in general:</p>

<ol class="index-list">
    <li>First, you need the right applications to extract the password protected sources_#########.zip archive. WinZip
        9+ (Windows) and Stuffit Expander 10+ (Mac) is the required software.
    </li>
    <li>You may also need Adobe Photoshop application. It is used to edit .PSD source files and it is necessary if you
        want to edit template design graphics and images.
    </li>
    <li>To edit template source code files, you need some code editor like Sublime Text, Adobe Dreamweaver (code mode
        only), Notepad++, etc.
    </li>
    <li>To upload files to the hosting server, you may need an FTP Manager like Total Commander, FileZilla, CuteFTP,
        etc.
    </li>
</ol>


<h4>Hosting</h4>
<p>As Zen Cart is a PHP/MySQL based application, you need to prepare a hosting environment to run Zen Cart.</p>
<p>If you have a live hosting, please make sure it matches Zen Cart software requirements and it is ready to be used for
    Zen Cart websites.</p>
<p>Otherwise you can run Zen Cart locally on your computer using the local server. To create a local hosting server,
    please use the localhost applications as <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b>, etc. Any of those can be easily
    installed as any other program and used to run Zen Cart. </p>
