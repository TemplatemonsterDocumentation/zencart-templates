<h3>How to install a Zen Cart Template.</h3>
<p>First, you need to download the purchased template onto your computer. The download link is available on your order
    details page.</p>
<p>When the template download is complete, you need to extract the files from the downloaded package. This can be done
    using the <a href="/help/unzipping-your-template.html" target="_blank">WinZip (Windows OS)</a> or <a
        href="/help/how-unzip-template-mac.html" target="_blank">Stuffit Expander (MAC OS)</a> applications. Don't
    forget to extract the files from the inner sources_XXXX.zip package. The sources archive is password protected and
    contains the template source files. The password can be obtained from the order details page. </p>
<ol class="index-list">
    <li><p>Open the '<b>themeXXX</b>' folder and upload the '<b>includes</b>' folder to Zen Cart installation root on
            your server.</p>

        <p class="alert alert-info">Check your 'admin' folder, in our template the folder is called 'admin123', it is
            necessary that the folder names match. If the folders are named differently, rename them so that they are
            identical.</p>

        <p class="alert alert-warning">Please note: Replacement of these files will not damage your store.</p>

        <p></p>
    </li>

    <li><p>Then open Zen Cart store admin panel and go to '<b>Tools > Template Selection</b>'.</p></li>

    <li><p>In the right column click <b>Edit</b> button and in the appeared select box choose a new template
            (themeXXX).
        </p>

